#define CP437_MAP_COUNT (sizeof(cp437_map) / sizeof(cp437_map[0]))

static unsigned short get_start_bucket(small_cp_t u)
{
    return (unsigned short)(((u * 158UL) & 0x03fe) >> 1);
}

static void add_to_xlate_table(small_cp_t u, unsigned char c)
{
    unsigned short bucket;

    /* Leave one slot empty so searches always terminate */
    if (xlate_table_items >= XLATE_TABLE_LEN - 1)
        return;

    bucket = get_start_bucket(u);

    while (xlate_table[bucket].codepoint != 0) {
        if (xlate_table[bucket].codepoint == u)
            return; /* already mapped */
        bucket++;
        if (bucket == XLATE_TABLE_LEN) bucket = 0;
    }

    xlate_table[bucket].codepoint = u;
    xlate_table[bucket].display = c;
    xlate_table_items++;
}

void unicode_init(void)
{
    unsigned short i;

    memset(xlate_table, 0, sizeof(xlate_table));
    xlate_table_items = 0;

    for (i = 0; i < CP437_MAP_COUNT; i++)
        add_to_xlate_table(cp437_map[i].codepoint, cp437_map[i].display);

    unicode_enabled = 1;
}

int unicode_xlate_loaded(void)
{
    return unicode_enabled && xlate_table_items > 0;
}

void unicode_disable(void)
{
    unicode_enabled = 0;
}

unsigned char unicode_find_display_char(small_cp_t u)
{
    unsigned short bucket = get_start_bucket(u);

    while (xlate_table[bucket].codepoint != 0) {
        if (xlate_table[bucket].codepoint == u)
            return xlate_table[bucket].display;
        bucket++;
        if (bucket == XLATE_TABLE_LEN) bucket = 0;
    }

    return UNICODE_TOFU;
}

unsigned char unicode_decode_utf8(const unsigned char *s, unicode_cp_t *cp)
{
    unsigned char c0 = s[0];

    if ((c0 & 0xE0) == 0xC0) {
        /* 2-byte: 110xxxxx 10xxxxxx */
        *cp = (unsigned long)((c0 & 0x1F) << 6) | (s[1] & 0x3F);
        return 2;
    } else if ((c0 & 0xF0) == 0xE0) {
        /* 3-byte: 1110xxxx 10xxxxxx 10xxxxxx */
        *cp = (unsigned long)(c0 & 0x0F) << 12
            | (unsigned long)(s[1] & 0x3F) << 6
            | (s[2] & 0x3F);
        return 3;
    } else if ((c0 & 0xF8) == 0xF0) {
        /* 4-byte: 11110xxx 10xxxxxx 10xxxxxx 10xxxxxx */
        *cp = (unsigned long)(c0 & 0x07) << 18
            | (unsigned long)(s[1] & 0x3F) << 12
            | (unsigned long)(s[2] & 0x3F) << 6
            | (s[3] & 0x3F);
        return 4;
    }

    *cp = 0xFFFFFFFFUL;
    return 1;
}
